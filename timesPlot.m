function timesPlot
    set(get(gca, 'title'), 'FontSize', ...
        20, 'FontName', 'Times');
    set(get(gca, 'xlabel'), 'FontSize', 20, ...
        'FontName', 'Times');
    set(get(gca, 'ylabel'), 'FontSize', 20, ...
        'FontName', 'Times');
    set(gca, 'FontSize', 20, 'FontName', 'Times');
end

